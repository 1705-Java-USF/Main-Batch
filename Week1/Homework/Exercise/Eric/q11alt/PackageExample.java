package q11alt;

// alt package to test retrieving variables/methods from other packages
public class PackageExample {
	// uses static floats and methods for simplicity's sake
	static public float f1 = 0.6f;
	static public float f2 = 2.3f;
	
	static public float sum(){ // sum of f1 and f2
		return f1 + f2;
	}
}
